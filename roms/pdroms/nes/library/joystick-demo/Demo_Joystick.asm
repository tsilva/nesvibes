;**************************************************************  **
;------------------------------------------------------------------:

Main:				;**-Main code loop**

   lda #$10
   jsr KillTm

 jsr JoyStickRead

 jsr FilterJoystick



    jmp Main

;------------------------------------------------------------------:
;**************************************************************  **
;------------------------------------------------------------------:

loadChar:			;**-Load Characters**
 lda Characters,y
 sta $2007
 iny
 dex
 bne loadChar

 jsr PPUon

 rts

;------------------------------------------------------------------:

Vblank:				;**-Vertical Blank Period**

 lda $2002
 bpl Vblank
 rts

;------------------------------------------------------------------:

PPUoff:				;**-Turn OFF PPU**

 lda #$00
 sta $2000
 sta $2001
 rts

;------------------------------------------------------------------:

PPUon:				;**-Turn ON PPU**

 lda #%00001110
 sta $2001
 stx $2005
 stx $2005
 rts

;------------------------------------------------------------------:

VRAMpal:			;**-Points to the Pallette in VRAM**

 lda #$3F
 sta $2006
 lda #$00
 sta $2006
 rts

;------------------------------------------------------------------:

LoadPal:			;**-Load Pal	
 lda pal,x
 sta $2007
 inx
 cpx #$16
 bne LoadPal


 lda #$20
 sta $2006
 lda #$C7
 sta $2006

 rts

;------------------------------------------------------------------:

KillTm:				;**-Waste Time**

 sta KillTimeY
 sta KillTimeX

 ldy KillTimeY
 ldx KillTimeX

One:
 JSR Two
 DEX
 BNE One
 DEY
 BNE One
Two:
 RTS

;------------------------------------------------------------------:

ChangePallette:

 jsr Vblank
 jsr VRAMpal

 ;lda PalletteSet
 ;tax

 jsr LoadPal

 rts

;------------------------------------------------------------------:

JoyStickRead:			;**-Get Joystick Status**

 lda #$01	; directly loads the Accumulator with 1
 sta $4016	; stores the Accumulator value (1) into $4016
 lda #$00	; directly loads the Accumulator with 0
 sta $4016	; stores the Accumulator value (0) into $4016

 ldx #$08	; loads 8 so it can loop for each byte

joyloop:
 lda $4016	; loads the info from the joystick
 and #$03	; ||
 cmp #$01	; narrows down the data
 rol $02	; rotates info into memory...
 dex 		; decrements X 
 bne joyloop	; loops again if not 0 

 lda $02
 sta Joystick1

 rts 		; returns to main routine

;------------------------------------------------------------------:

FadeIn:
;---
 lda #$4
 sta LoopCount

FadeInLow:

   lda #$10
   jsr KillTm

   clc
   lda PalletteSet
   adc #$04		;get next pal set (forward)
   sta PalletteSet
   tax

   jsr ChangePallette

   ldy TextPointer
   ldx #$11
   jsr loadChar

 dec LoopCount
 bne FadeInLow



 lda #$4
 sta LoopCount

FadeOut:
;---
   lda #$10
   jsr KillTm

   sec
   lda PalletteSet
   sbc #$04		;get next pal set (backward)
   sta PalletteSet
   tax
   jsr ChangePallette

   ldy TextPointer
   ldx #$11
   jsr loadChar

 dec LoopCount
 bne FadeOut

EndFade:
 rts

;------------------------------------------------------------------:

FilterJoystick:

Acheck:	  			;A Button
 lda Joystick1
 and #$80
 beq Bcheck

 and LastButton
 bne Bcheck

 lda #$00
 sta TextPointer
 jsr FadeIn
 jmp Xcheck

Bcheck:	  			;B Button?
 lda Joystick1
 and #$40
 beq Slcheck

 and LastButton
 bne Slcheck

 lda #$11
 sta TextPointer
 jsr FadeIn
 jmp Xcheck

Slcheck:  			;Select Button?
 lda Joystick1
 and #$20
 beq Stcheck

 and LastButton
 bne Stcheck

 lda #$22
 sta TextPointer
 jsr FadeIn
 jmp Xcheck

Stcheck:  			;Is the Start Button?
 lda Joystick1
 and #$10
 beq Ucheck

 and LastButton
 bne Ucheck

 lda #$33
 sta TextPointer
 jsr FadeIn
 jmp Xcheck

Ucheck:	 			;Is the Up Arrow?
 lda Joystick1
 and #$08
 beq Dcheck

 and LastButton
 bne Dcheck

 lda #$44
 sta TextPointer
 jsr FadeIn
 jmp Xcheck

Dcheck:	  			;Is the Down Arrow?
 lda Joystick1
 and #$04
 beq Lcheck

 and LastButton
 bne Lcheck

 lda #$55
 sta TextPointer
 jsr FadeIn
 jmp Xcheck

Lcheck:	  			;Left Arrow?
 lda Joystick1
 and #$02
 beq Rcheck

 and LastButton
 bne Rcheck

 lda #$66
 sta TextPointer
 jsr FadeIn
 jmp Xcheck

Rcheck:	  			;Right Arrow?
 lda Joystick1
 and #$01
 beq Xcheck

 and LastButton
 bne Xcheck

 lda #$77
 sta TextPointer
 jsr FadeIn
 jmp Xcheck

Xcheck:
 sta LastButton
 rts

