;-INES header setup

 .inesprg    1   ; Specifies the number of 16k prg banks.
 .ineschr    1   ; Specifies the number of 8k chr banks.
 .inesmir    0   ; Specifies VRAM mirroring of the banks.
 .inesmap    0   ; Specifies the NES mapper used.

 .bank 0
 .org $8000

;------------------------------------------------------------------:

KillTimeX   = $00
KillTimeY   = $01

Joystick1   = $0F

LastButton  = $28
CurrButton  = $2f

PalletteSet = $30

LoopCount   = $3f

TextPointer = $40

;------------------------------------------------------------------:

RESET
 cld
 sei


 jsr Vblank

 jsr PPUoff

 jsr VRAMpal

 ldx #$00

 jsr LoadPal

 ldy #$00
 ldx #17
 jsr loadChar

